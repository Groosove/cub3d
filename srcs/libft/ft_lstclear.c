#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list *list;

	if (del != 0)
		while (*lst != 0)
		{
			del((*lst)->content);
			list = *lst;
			*lst = list->next;
			free(list);
		}
	*lst = 0;
}
